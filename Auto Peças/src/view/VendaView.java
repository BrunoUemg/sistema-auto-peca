    public void readJTableProduto(String nome) throws SQLException {
        DefaultTableModel modelF = (DefaultTableModel) tbProdutos.getModel();
        modelF.setNumRows(0);
        for (Produto p : produtoDAO.readProdutoLikeEstoque(nome)) {
            modelF.addRow(new Object[]{
                p.getIdProduto(),
                p.getNome(),
                p.getQuantidadeEstoque(),
                p.getNomeCategoria(),
                p.getNomeMarca()
            });

        }
    }

    public Double updateTotal() {
        double total = 0;
        for (ItensVenda i : venda.getItensVenda()) {
            total += (double) i.getPrecoUnitario() * (double) i.getQuantidade();
        }
        return total;
    }

    public void readJTableCliente(String nome) throws SQLException {
        DefaultTableModel modelF = (DefaultTableModel) tbCliente.getModel();
        modelF.setNumRows(0);
        for (Cliente c : clienteDAO.readCliente(nome)) {
            modelF.addRow(new Object[]{
                c.getIdCliente(),
                c.getNome(),
                c.getTelefone(),
                c.getCelular(),
                c.getEmail(),
                c.getCidade()
            });

        }
    }

    public void readTabelaItens() throws SQLException {
        DefaultTableModel modelItens = (DefaultTableModel) tbItens.getModel();
        modelItens.setNumRows(0);
        for (ItensVenda i : venda.getItensVenda()) {
            modelItens.addRow(new Object[]{
                i.getProduto().getNome(),
                i.getQuantidade(),
                i.getPrecoUnitario(),
                i.getPrecoUnitario() * i.getQuantidade()
            });
        }
    }


