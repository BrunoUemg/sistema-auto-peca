    private void btnaddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddItemActionPerformed
        if (txtProduto.getText().isEmpty() || txtValorItem.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos vazios!!");
        } else {
            if (Integer.parseInt(txtQuantVenda.getText()) <= produto.getQuantidadeEstoque()) {
                itemVenda = new ItensVenda();
                itemVenda.setProduto(produto);
                itemVenda.setPrecoUnitario(Float.valueOf(txtValorItem.getText()));
                itemVenda.setQuantidade(Integer.valueOf(txtQuantVenda.getText()));
                venda.getItensVenda().add(itemVenda);
                txtData.disable();
                txtValorTotal.disable();
                btnaddSelectFornecedor.disable();
                addCliente.setEnabled(false);
                txtProduto.setText("");
                txtProduto.enable();
                txtQuantVenda.setText("");
                txtValorItem.setText("");
                try {
                    readTabelaItens();
                } catch (SQLException ex) {
                    Logger.getLogger(VendaView.class.getName()).log(Level.SEVERE, null, ex);
                }
                preparaAdd();
                txtValorTotal.setText("Valor Total: " + String.valueOf(updateTotal()));
            }else{
                txtQuantVenda.setText(String.valueOf(produto.getQuantidadeEstoque()));
                JOptionPane.showMessageDialog(null, "Verifique a quantidade, estoque menor que o informado!");
            }
        }
    }//GEN-LAST:event_btnaddItemActionPerformed

    private void btnremoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveItemActionPerformed
        if (tbItens.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela");
        } else {
            venda.getItensVenda().remove(tbItens.getSelectedRow());
            try {
                readTabelaItens();
            } catch (SQLException ex) {
                Logger.getLogger(VendaView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnremoveItemActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtVendedor.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Adicione um produto na tabela");

        } else {
            int confirma = JOptionPane.showConfirmDialog(null, "Deseja confirmar essa venda?");
            if (confirma == 0) {
                String text1 = txtData.getText();
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                java.util.Date textFieldAsDate1 = null;
                try {
                    textFieldAsDate1 = sdf.parse(text1);
                } catch (ParseException ex) {
                    Logger.getLogger(VendaView.class.getName()).log(Level.SEVERE, null, ex);
                }
                venda.setDataVenda(textFieldAsDate1);
                venda.setIdFunc(Session.getInstance().getIdFuncionario());
                venda.setValorTotal(updateTotal());
                venda.setCli(cliente);
                try {
                    vendaDAO.salvarVenda(venda);
                } catch (SQLException ex) {
                    Logger.getLogger(VendaView.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "Venda cadastrada com sucesso!");
                desabilita();
                limpaTb();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

